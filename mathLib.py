import math
#using math library and some basic function of this
print(abs(-45))
print(math.fabs(-45))

print(math.ceil(-45.28))
print(math.ceil(200.12))   #ceil prints the nearest maximum integer


print(math.floor(-45.12))
print(math.floor(200.23)) #floor prints the nearest minimum integer

print(math.acos(1))
print(max(10, 20, 30, 40, 50))